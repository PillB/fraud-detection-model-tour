"""
Run All Model Tour Examples
===========================

Executes every lightweight fraud/anomaly/graph example, captures metrics from
stdout, and writes a consolidated Markdown report plus a PR-AUC visualization.

Run locally:
    python experiments/run_all_model_examples.py

Run online:
    Open notebooks/model_tour_examples.ipynb in Google Colab.
"""

import os
import re
import subprocess
import sys
from pathlib import Path

os.environ.setdefault("MPLCONFIGDIR", str(Path(__file__).resolve().parent / ".mplconfig"))

import matplotlib

matplotlib.use("Agg")
import matplotlib.pyplot as plt


ROOT = Path(__file__).resolve().parents[1]
EXPERIMENTS = ROOT / "experiments"

SCRIPTS = [
    ("Isolation Forest", "toy_isolation_forest.py"),
    ("XGBoost / GBDT", "toy_xgboost.py"),
    ("Supervised Baselines", "toy_supervised_baselines.py"),
    ("Classical Anomaly Suite", "toy_classical_anomaly_suite.py"),
    ("Density Outliers", "toy_density_outlier_suite.py"),
    ("VAE / Reconstruction", "toy_vae.py"),
    ("LSTM Sequence Proxy", "toy_lstm_seq.py"),
    ("TabTransformer Proxy", "toy_tabtransformer.py"),
    ("MoE Hybrid", "toy_moe_hybrid.py"),
    ("GraphSAGE Proxy", "toy_graphsage.py"),
    ("Community + Role Detection", "toy_community_role_detection.py"),
    ("Temporal Graph Risk", "toy_temporal_graph_risk.py"),
    ("Graph Link Prediction", "toy_graph_link_prediction.py"),
    ("GraphRAG Analyst", "toy_graphrag_analyst.py"),
    ("Entity Resolution", "toy_entity_resolution.py"),
    ("Comparison Harness", "compare_models.py"),
]


def extract_metrics(output):
    metrics = {}
    for pattern, name in [
        (r"PR-AUC(?: \(Average Precision\))?(?: on test)?\s*[:=]\s*([0-9.]+)", "PR-AUC"),
        (r"Link prediction PR-AUC=([0-9.]+)", "PR-AUC"),
        (r"ROC-AUC=([0-9.]+)", "ROC-AUC"),
        (r"Best F1 \(on scores\):\s*([0-9.]+)", "Best F1"),
        (r"Recall@50=([0-9.]+)", "Recall@50"),
    ]:
        hits = [float(x) for x in re.findall(pattern, output)]
        if hits:
            metrics[name] = max(hits)
    return metrics


def run_script(label, script):
    result = subprocess.run(
        [sys.executable, str(EXPERIMENTS / script)],
        cwd=ROOT,
        capture_output=True,
        text=True,
        timeout=120,
        check=False,
    )
    output = result.stdout + ("\nSTDERR:\n" + result.stderr if result.stderr else "")
    return {
        "label": label,
        "script": script,
        "returncode": result.returncode,
        "output": output,
        "metrics": extract_metrics(output),
    }


def write_report(results):
    report_path = EXPERIMENTS / "all_model_examples_summary.md"
    lines = [
        "# All Model Example Run Summary",
        "",
        "Generated by `experiments/run_all_model_examples.py`.",
        "",
        "| Example | Script | Status | Key metrics |",
        "|---|---|---:|---|",
    ]
    for result in results:
        status = "PASS" if result["returncode"] == 0 else "FAIL"
        metric_text = ", ".join(f"{k}: {v:.4f}" for k, v in result["metrics"].items()) or "n/a"
        lines.append(f"| {result['label']} | `{result['script']}` | {status} | {metric_text} |")
    lines.append("")
    lines.append("## Notes")
    lines.append("")
    lines.append("- PR-AUC is the primary fraud metric because the positive class is rare.")
    lines.append("- GraphRAG and entity-resolution examples produce evidence outputs rather than classifier PR-AUC.")
    lines.append("- Heavy production upgrades such as PyOD, PyG/DGL, LightGBM/CatBoost, and full TGN are documented as optional extensions.")
    report_path.write_text("\n".join(lines) + "\n", encoding="utf-8")
    return report_path


def write_plot(results):
    rows = [(r["label"], r["metrics"]["PR-AUC"]) for r in results if "PR-AUC" in r["metrics"]]
    rows = sorted(rows, key=lambda x: x[1], reverse=True)
    plot_path = EXPERIMENTS / "all_model_examples_summary.png"

    plt.figure(figsize=(10, max(4, len(rows) * 0.38)))
    plt.barh([r[0] for r in rows], [r[1] for r in rows], color="#1e40af")
    plt.gca().invert_yaxis()
    plt.xlabel("Best PR-AUC found in script output")
    plt.title("Fraud Detection Model Tour - Runnable Example Metrics")
    plt.xlim(0, max([r[1] for r in rows] + [0.1]) * 1.15)
    plt.tight_layout()
    plt.savefig(plot_path, dpi=140)
    return plot_path


def main():
    print("=== Running all Fraud Detection Model Tour examples ===")
    results = []
    for label, script in SCRIPTS:
        print(f"\n--- {label}: {script} ---")
        result = run_script(label, script)
        print("status:", "PASS" if result["returncode"] == 0 else "FAIL")
        if result["metrics"]:
            print("metrics:", ", ".join(f"{k}={v:.4f}" for k, v in result["metrics"].items()))
        else:
            print("metrics: evidence/output example")
        if result["returncode"] != 0:
            print(result["output"][-1600:])
        results.append(result)

    failures = [r for r in results if r["returncode"] != 0]
    report = write_report(results)
    plot = write_plot(results)
    print("\nWrote report:", report)
    print("Wrote visualization:", plot)
    if failures:
        raise SystemExit(f"{len(failures)} examples failed")
    print("ALL_MODEL_EXAMPLES_COMPLETED")


if __name__ == "__main__":
    main()
